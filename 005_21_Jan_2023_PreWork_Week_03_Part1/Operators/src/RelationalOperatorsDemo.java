

public class RelationalOperatorsDemo {

	public static void main(String[] args) {
		
		// less than
		boolean b = 30 < 80;
		System.out.println("Value is " + b);
		
		// greater than
		b = 80 > 30;
		System.out.println("Value is " + b);
		
		// greater than or equal to
		b = (3 >= 3);
		System.out.println("Value is " + b);

		// less than or equal to
		b = (5 <= 10);
		System.out.println("Value is " + b);

		// equality
		b = (3 == 10);
		System.out.println("Value is " + b);

		// not equal
		b = (10 != 10);
		System.out.println("Value is " + b);
		
		
	}
}
